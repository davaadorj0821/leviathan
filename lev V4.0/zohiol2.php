			<p class="read-more-wrap">Нэгдүгээр хэсэг – Вушан тосгоны өглөө.

Юлан тивийн хамгийн том уул нуруу  Шидэт араатнуудын уулсын баруун зүгт байрлах Фэнлай улсын жижигхэн тосгон Вушан.

Өглөөний нар мандахтай зэрэгцэн Вушан хотод өглөөний сэрүү татсан байлаа. Гэхдээ тосгоны бүх иргэд аль хэдийн босч ажилдаа орсон байв. Зургаагаас долоо настай хүүхдүүд хүртэл орноосоо босон уламжлалт өглөөний дасгалаа хийхээр зэхэж байв.

Вушан тосгоны зүүн зүгийн хоосон талбайд өглөөний нар дулаахнаар ээнэ. Нарны туяа хүрээлэх модод болон навчсын дундуур нэвчин газарт хэсэг хэсэг газруудад л тусна.

Тэнд бүлэг хүүхдүүд харагдах ба ойролцоогоор хоёр зуугуулаа байв. Энэ бүлэг хүүхдүүд гурван хэсэг болсон ба хэсэг тус бүрийн хүүхдүүд хэд хэдэн эгнээ болон эгнэн зогссон байна. Хамгийн хойд хэсэгт ойролцоогоор зургаан настай хүүхдүүд. Голын хэсэгт есөөс арван хоёр настай хүүхдүүд. Урд талын хэсэгт арван гурваас арван зургаан настнууд байв.

Эдгээр бүлэг хүүхдүүдийн өмнө гурван өндөр хөгжсөн бие бялдартай дунд насны залуус байв. Тэд гурвуулаа богино цамц, болхи гэмээр тайрсан өмдтэй.

“Хэрэв хүчирхэг дайчин болохыг хүсвэл багаасаа шаргуу хичээллэх хэрэгтэй.” Дунд насны залуусын ахлагч толгойгоо өндөрт өргөн, гараа нурууны ардаа барин хүйтнээр хэллээ. Тэрээр хүйтэн хөндий харцаа хамгийн хойно байрлах хэсэг хүүхдүүд уруу чиглүүлнэ. Зургаагаас долоон настнууд бүгд уруулаа жимийсэн байв. Том дугариг нүдээрээ дунд насны залууг хараад хүүхдүүд авиа гаргахаас ч эмээнэ.

Ахлагчийн нэрийг Хиллман гэдэг. Тэрээр Вушан тосгоныг захирдаг язгууртан Баруч овгийн хамгаалалтын ахмад байлаа.

<span class="read-more-target">“Та бүхэн жирийн иргэдийн хүүхдүүд. Язгууртан айлын хүүхдүүдээс ялгаатай нь та нар тулааны Кид¹  хэрхэн сурах нууц гарын авлага олж чадахгүй. Үнэлэгдэхүйц хүн болохыг хүсч байгаа бол! Хүндлэгдэхийг хүсч байгаа бол та нар хамгийн эртний бөгөөд хамгийн энгийн аргаар өөрсдийгөө хөгжүүлэх хэрэгтэй. Энэ арга бол өөрсдийнхөө биеийг хөгжүүлж хүч чадалтай болох. Ойлгомжтой юу?!!”

Хилман хүүхдүүдийг нэг тойрон ажив.

“Ойлголоо” гэж нэгэн дуунаар хүүхдүүд хариулав.

“Сайн байна” сэтгэл хангалуун Хилман толгой дохив. Зургаан настнууд эргэлзэж байсан бол өсвөр насны хүүхдүүд машид тууштай урам зоригтой байлаа. Учир нь тэд Хилманы үг ямар утга учиртайг ойлгож байгаа.

Ерөнхийдөө Юлан тивийн бүх хүүхдүүд маш багаасаа шаргуу дасгал хийдэг. Хэрэв залхуурвал ирээдүйд бусдын дор орно. Мөнгө, эрх мэдэл энэ эр хүний зэрэг зиндааг илэрхийлдэг зүйлс. Харин хүч чадалгүй эр хүн эмэгтэй хүнд хүртэл ад үзэгдэнэ.

Хэрэв эцэг эхээрээ бахархуулж, эмэгтэй хүнээр хүндлүүлэн, бардам амьдралаар амьдрахыг хүсч байвал тэд хүчирхэг дайчид болох ёстой байлаа.

Тэд эгэл ардын хүүхдүүд. Тулааны Кид суралцах нандин гарын авлагууд тэдэнд хүртээлгүй. Нэр алдарт хүрэх цорын ганц арга бол маш багаасаа хүч чадалтай болох байв. Язгууртнуудаас илүү шаргуу ажиллаж, бүхий л эрч хүч өөрсдийн хөлс цусаа өөрсдийгөө хүчирхэгжүүлэхэд зориулах ёстой!

“Өглөө нар мандахад бүх зүйлс сэргэдэг. Энэ үед эргэн тойрноосоо байгалийн энергийг сорон авч өөрийнхөө бие бялдараа хөгжүүлэх нь хамгийн ашигтай. Үргэлж санаж яв. Хөлөө мөрний хэмжээний зайтай. Хоёр өвдөг бага зэргийн суулттай. Хоёр гар бүсэлхийгээ тулсан байрлалтай. Энэ бол “Ки бүрэлдүүлэх зогсолт”. Зогсолтыг авахаараа санаж яв. Анхаарлаа төвлөрүүлж, бодол санаагаа тогтуун барьж, ердийн үеийхээрээ амьсгалах ёстой.” гэж Хилман хүйтнээр сургалаа.

Ки бүрэлдүүлэх зогсолт бол хүний биеийг хөгжүүлэх хамгийн энгийн бөгөөд хамгийн үр дүнтэй арга байв. Энэ үе үеийн өвөг дээдсийн туршлага дээр тулгуурласан арга зүй юм.

Тэр даруйд хоёр зуун хүүхдүүд Ки бүрэлдүүлэх зогсолтыг авлаа.

“Санаж яв анхаарлаа төвлөрүүлж, бодол санаагаа тогтуун байлгаж, ердийн үеийнхээрээ амьсгал.” гэж Хилман хүүхдүүдийн дундуур алхаж байхдаа хүйтнээр санууллаа.

Тэрээр өмнөд хэсгийн хүүхдүүдийг тогтуун амьсгалж, зогсолтоо тэнцвэртэй барьж байхыг харлаа. Яг энэ үед зогсолтоо тогтвортой барих зорилгодоо бүгд хүрлээ. Ки бүрэлдүүлэх зогсолтыг авах тодорхой хэмжээний ур чадварыг урд хэсгийн хүүхдүүд хуримтлуулсан нь толдорхой харагдаж байв.

Харин хойно байх хэсэг хүүхдүүдийг харвал бүсэлхий болон өвдөгүүд нь зарим өнцгөөр гажиж, хөл нь сул болон сулбагар байв. Хилманы хувьд тэд тогтворгүй байрлалаар хүчгүй зогсож байгаа нь илт байв.

Хилман нөгөө дунд насны хоёртоо “Та хоёр урд болон дунд байгаа бүлэг хүүхдүүдийг хариуцаж ав. Би хамгийн балчируудыг нь хариуцлаа”

“Мэдлээ ахмадаа” Дунд насны нөгөө хоёр тэр даруйдаа Хилманы хэлсэн үгийг дагаж урд байх хоёр хэсэг хүүхдүүдэд багшилна. Тэр хоёр үе үе хүүхдүүдийн хөлрүү өшиглөж бат бөх зогсож байгаа эсэхийг шалгана.

Хилман хойд хэсэгт байх бүлэг хүүхдүүд уруу алхвал хүүхдүүд дор дороо сандарцгаана.

“Чөтгөр алгад, ахлагч мангас нь одоо ирж байна даа”  Алтан шар үстэй, хурц нүдтэй Хадли намуухан дуугаар шившнэлээ.

Хилман хүүхдүүдийн дундуур алхана. Тэрээр гаднаа маш хүйтэн царайтай харагдаж байлаа. Гэхдээ тэрээр дотроо санаа алдан “Энэ хүүхдүүд хэтэрхий балчир юм. Ухаан биеийн хүч хоёрын аль алин энэ хүүхдүүдэд байхгүй. Би хэтэрхий их зүйл шаардаж, чангалж болохгүй. Гэхдээ л багаасаа бэлдэж байгаа нь энэ хүүхдүүдэд тустай. Одоо багаасаа бэлдэж байгаа нь ирээдүйд тэд тулаанд ороод амьд үлдэх боломжийг нэмж өгнө шүү дээ.”

Мөн хүүхдүүдэд сайн багшлахын тулд хамгийн үр дүнтэй аргыг сонгох хэрэгтэй. Хамгийн үр дүнтэй арга бол сурч байгаа зүйлдээ сонирхолтой болгох явдал. Хэрэв хэтэрхий их шахвал эсрэгээрээ муудчихаж ч мэднэ.

“Бүгд сонсоцгоо. Номхон зогс!” Хилман хүйтнээр тушаалаа.

Тэр даруйд бүх хүүхдүүд цэх зогсож, цээжээ тэгшилж, чанх урагш ширтэцгээв.

Хилман хөнгөхөн инээмсэглэж байгааг уруулнаас нь харж болно. Тэрээр хүүхдүүдийн урд очиод, цамцаа тайллаа. Хүчтэй бие, бадьруун булчингууд, мөн түүн дээрх тэгш сайхан зураасууд хүүхдүүдийн нүдийг бүлтийлгэн бишрүүлэв.

Түүний төгс биеийн хэлбэрээс гадна, Хилманы цээжэн хэсэгт тоо томшгүй хутга болон сэлэмний сорвинууд байлаа. Хүүхдүүд түүний сорвийг нүдээ гялалзуулан ширтэж байв.

Хутганы сорви, Сэлэмний сорви. Энэ бол эр хүний гавьяаны медал юм!

Хүүхдүүдийн сэтгэлд Хилманийг бишрэн шүтэх сэтгэл дүүрэн байв. Амьдрал болон үхлийн заагт тэмцэж дайтсан зургадугаар зэргийн хүчирхэг дайчин! Тэр байтугай томоохон хотуудад ч гэсэн тэрээр гайхалтай хүн байх байсан. Жижигхэн Вушан тосгонд тэрээр олон хүнийг бишрүүлсэн эр хүн байлаа.

Хүүхдүүдийн биширсэн харцуудыг хараад Хилман өөрийн эрхгүй хөнгөхөн инээмсэглэв. Тэрээр бишрэх мэдрэмжийг хүүхдүүдэд сийлэхийг хүсч байв. Тэрээр хүүхдүүдийг өөр шигээ хүчирхэг болох хүслийг бадраахийг хүсч байв. Ингэж байж хүүхдүүд улам шаргуу хичээж, сэдэлжих юм.

“За гал дээр дахиад тос нэмье!” Хилман дотроо нууцхан хэлээд урд байх томоохон бул гурван зуугаас дөрвөн зуун паунд чулуун дээр очив.

Хилман ганц гараараа бул чулууг барин авч. Маш тайвнаар чулууг харж байв. Хилманы гарт гурван зуун паунд том бул чулуу яг л мод шиг хөнгөхөн харагдаж байв. Бүх хүүхдүүдийн эрүү унаж, нүд нь бүлтэрлээ.

“Хэтэрхий хөнгөхөн юм. Лори хэрэв дасгалийн дараа чамд сул цаг байвал надад илүү том бул чулуу авчраарай.” Хөнгөхөн шидэлтээр Хилман өнөөх том бул чулууг хэдэн метрийн цаана шидлээ. Нир хийн дуугарлаа! Бул чулуу томоохон модны өмнө газардаж, бүхий л газар чичирхийллээ. Хилман дахиад бул чулуудын өмнө алхаж байснаа.

“Ха!”

Хилман гүнзгий амьсгаа авч. Түүний булчинлаг биеийн бүх судас томрон гарч ирж, хажуудаа байгаа цэнхэр том хад чулуу уруу хүчтэй цохино. Түүний атга ойр орчмын агаарыг хуйларуулж, хүүхдүүдэд сонсогдохуйц агаарын исгэрэх дуу сонсогдоно. Хилманы хүчтэй атга бул чулууг цохилоо.

Нир хийж дуугарлаа!!! Энэ чулууг цохих гарын цохилт хүүхдүүдийн зүрхийг чичирхийлгэв.

Энэ чинь маш хатуу цэнхэр өнгийн бул чулуу байсан шүү дээ.

Цэнхэр өнгийн бул чулуу чичирхийлж, удалгүй зургаа эсвэл долоон томоохон цууралт авч, Пин хийх дуугаар том бул чулуу дөрвөөс таван хэсэгт хуваагдлаа. Гэхдээ Хилманы гарт шалбархай ч харагдахгүй байв.

“Ахмад гайхалтай хэвээрээ байна.” Хоёр дунд насны нэг болох Лорри хөхрөн инээж хэллээ. Хилман хоёр дунд насны хүмүүс уруу алхаж эхэллээ.

Нөгөө дунд насны залуу Рожер мөн алхана. Ихэнхидээ хүүхдүүд Ки бүрэлдүүлэх зогсолтыг сургууллана. Хүүхдүүдийг сургуулилах үед тэр гурав амран, чөлөөтэй хоорондоо ярьж, залхуурсан хүүхдүүдэд анхаарал хандуулдаг байв.

Хилман толгойгоо сэгсрэн, инээгээд.  “Үгүй байлгүй дээ. Өнгөрсөнд би армид алба хаадаг байхдаа өдөр бүр галзуу мэт сургуулилдаг байсан. Харин дайны талбарт би цуст гардан тулаанд олон ордог байж дээ. Одоо би харин өглөө босоод булчингаа ганц л сунгах юм. Өнгөрсөн шигээ эрч хүчтэй байж чадахгүйн дээ.”

Бүх хүүхдүүд Хилманыг шүтэж биширсэн харцаар харж байв.

Цэнхэр том бул чулуу түүний ганц цохилтоор бутран уналаа. Энэ ямар гээчийн хүч бэ? Мөн гурван зуугаад паундийн хүнд чулуу түүний ганц гараар хөнгөхөн шидэгдлээ шүү дээ. Энэ ямар гээчийн хүч бэ?


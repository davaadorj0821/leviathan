“Өнөөдөр би тивийн бүх хүн мэддэг домогт дөрвөн эрхэм дайчдийн талаар ярьж өгөх болно.” Сүрдмээр биширмээр дүрийг Хилманаас харж болох байлаа.

Хүүхдүүдийн чих дэлдийж, нүд нь сэргэв. Линлей газар сууж, зүрхээ хүчтэй цохилохийг мэдрэнэ. “Домогт дөрвөн дээд дайчин?” Линлейгийн чих мөн чагнаж, Хилманаас нүдээ салгаж чадахгүй ирмэх ч үгүйгээр ширтэж байлаа.

Хилманы нүдэнд догдолсон байдал хальт ажиглагдана. Гэсэн хэдий ч намуухан хоолойгоор ” Бидний тивд, мянган мянган жилийн өмнө дөрвөн домогт дайчин гарч ирсэн. Энэ дөрвөн дайчин хүчирхэг луу лугаа адил хүчирхэг байж. Сая дайчидтай армийн эсрэг зогсож, маш амархнаар жанжиний толгойг авч чаддаг байлаа. Эдгээр дөрвөн дайчин Луугийн цуст дайчин, Ягаан галт дайчин, Луугийн судалт дайчин, мөн Үхэшгүй дайчин гэдгээрээ танигдсан”

“Дайчид есөн зэрэглэлд хуваагддаг. Би, зөвхөн зургадугаар зэрэглэлийн дайчин. Том модыг өшиглөн унагааж, том хадыг хэсэг хэсэг болгож чадна. Гэхдээ есдүгээр зэрэглэлийн дайчин манай Фэнлай улсад хамгийн шилдэг дайчиндаа ордог. Гэхдээ есдүгээр зэрэглэлийн дайчнаас илүүтэйгээр дөрвөн домогт дайчид орно. Тэд есдүгээр зэрэглэлийн дайчнийг давж, дайчдийн орой домогт Гэгээн зэрэглэлийн дайчид болдог!!” Хилманы нүд догдолсон байв. “Домогт Гэгээнтэн зэрэглэлийн дайчид мөсөн уулсыг хайлуулж, эцэсгүй далайг догшин давалгаагаар дүүргэн, том уулсыг чичирхийлүүлэн, сая хүн амтай хотыг сүйрүүлж, тэнгэрээс солир унагах хүчтэй. Тэд юунд ч дийлдэхгүй хамгийн хүчтэй билээ.”

Чив чимээгүй. Бүгд гайхшран хөшсөн байлаа.

Зүүн хойно байрлах уул уруу заан.

“Вушанийг харцгаа. Том биш байна гэж үү?” Хилман инээмсэглэв.

Химланы үгийг сонссон хүүхдүүд бага зэрэг бүрсхийн айлаа. Тэд бүгд тогой дохив. Вушан мянган метер өндөр бөгөөд мянга мянган метрийн урттай байлаа. Эр хүний нүдэнд энэ үнэхээр өндөр уул байлаа.

“Гэхдээ гэгээнтэн зэрэглэлийн дайчид нүд ирмэхийн дотор Вушан уулыг арчиж чадна” Хиллман шулуухан хэллээ.

Зургадугаар зэрэглэлийн дайчин зөвхөн бул чулуу бутлаж чадах бол гэгээнтэн зэрэглэлийн дайчин бүхэл бүтэн уул сүйтгэж чадна. Бүх хүүхдийн ам ангайж, нүд нь бүлтийв. Тэд бүгд балмагдлаа. Тэр даруйдаа хүүхдүүдийн зүрх гэгээнтэн зэрэглэлийн дайчдад хэлж боломгүй хүндлэл үзүүллээ.

“Уул сөнөөх өө?” Хилманы үгс Линлейд хүчтэй нөлөө үзүүлж байв.

Удаан хугацаа өнгөрөлгүй, алмайрч хөшсөн хүүхдүүд гэр гэрлүүгээ харьцгаалаа. Хилман, Рожер, Лорри гурав хамгийн сүүлд харьцгаав. Гурав таваараа бүлэг болон харьцгаах хүүхдүүдийг хараад Хиллман инээмсэглэв.

“Энэ хүүхдүүд бол манай Вушан тосгоний ирээдүйн найдвар юм” Хилман инээмсэглэн хэлэв..

Рожер болон Лорри бүлэг хүүхдүүд уруу харцгаав. Энэ тивд ерөнхийдөө бүх жирийн ардын хүүхдүүд маш эрт цагаас сургуулилах хэрэгтэй болдлог. Энэ хүүхдүүдийг харан Рожер, Лорри хоёр өөрсдийнхөө залуу насаа бодлоо.

“Ахмад Хиллман, өнгөрсөн үеийн Поттероос хамаагүй илүү гэдэг нь гарцаагүй шүү. Таны чиглүүлгэ дор ойр хавийн арван тосгоноосоо ч илүү манай Вушан тосгон бүс нутгийнхаа хамгийн хүчирхэг тосгон болно гэдэгт итгэлтэй байна.” Лорри инээмсэглэн хэлэв.

Багшийн ур чадвар тосгоны ирээдүйг тодорхойлдог байлаа.

“Нээрээ та хэрвэн гэгээнтэн зэрэглэлийн дайчдийн хүч, бас дөрвөн домогт дайчний тухай мэддэг юм бэ?” Гэнэтхэн Лорри асуултаа саналаа.

Маш багахан улайж, Хиллман хоолойгоо зассанаа

“Юу, юулдаа. Би дөрвөн домогт дайчнийг үнэндээ хэр хүчтэй байснийг мэдэхгүй л дээ. Эцсийн эцэст энэ чинь домгийн зүйлс шүү дээ. Энэ хүмүүс үзэгдэхгүй болоод маш олон жил болсон”

Лорри Рожер хоёр гайхацгаан. “Та юу ч мэдэхгүй хэрнээ хүүхдүүдийг хуурлаа гэжүү??”

Хиллман бага зэрэг инээмсэглэснээ. “Би дөрвөн домогт дайчнийг хэр хүчтэйг мэдэхгүй ч, ямартай ч би гэгээнтэн зэрэглэлийн шидтэн нууц шидэт шившлэгээ ашиглан арван мянган хүнтэй армийг, эсвэл бүхэл хотыг устгах чадалтайг мэднэ. Нэгэнт гэгээнтэн зэрэглэлийн шидтэн ийм хүчтэй юм чинь гэгээнтэн зэрэгэлэлийн дайчин үүнээс тийм ч сул дорой байх ёсгүй гэж бодож байна.”

“ Хамгийн чухал нь энэ түүхийг хүүхдүүдийг улам шаргуу хичээгээсээ гэж хүссэндээ ярьж өгсөн. Энэ түүхүүдийг сонсоод ямар их гайхширснийг та нар харж чадахгүй байна гэжүү?” Хиллман багахан инээмсэглэв

Лорри болон Рожерт хэлэх ч үг олдсонгүй ээ..

…..

“Дараа уулзая Лей!”

“Дараа уулзая, Хадли”

Сайн найз Хадлитэйгээ салах ёс гүйцэтгээд Линлей ганцаараа Баручийн эзэмшлийн газарлуу гэртээ харьлаа. Хэсэг хугацааны дараа тэрээр Баручийн эзэмшлийн газарыг харав.

Баручийн эзэмшлийн байшин байрлах газар үнэн хэрэгтээ маш том байлаа. Гогцоодож ургадаг ургамлууд хананд ургаж, мөн янз бүрийн зэрлэг ургамал хана даган шигүү ургасан байв. Мөн цаг хугацаа хананд илт мэдэгдэхүйц ан цав үүсгэжээ. Вушан тосгонд байрлах энэ эртний өвөг дээдсийн гэр бол Баруч овгийн өвөг дээдсийн гэр билээ. Энэ эртний гэр таван мянган жил энд байрлаж байна.

Овгийн эзэмшил багассан нь мөн Баруч овгийн санхүүд хэцүү байдлийг дагуулжээ.Эцсийн эцэст өмнө олсон хөрөнгөө идэж дуусчээ. Зуун жилийн өмнө Баруч овгийн удирдагч бүх гишүүдээ зөвхөн урд талын эзэмшил буюу нийт эдлэнгийн гуравны нэг хэсэг газарт амьдрахыг шаардсан. Бүх талбайг эзлэн амьдрахгүй болсноор маш их мөнгийг хэмнэжээ.

Гэхдээ цаг хугацаа өнгөрөх тутам Линлейгийн аав Хогг Баруч одоо хүртэл гэр бүлийн эзэмшлээ заран амь зогоодог болов.

Эдлэнгийн гол хаалга онгорхой байв

“Гэгээнтэн зэрэглэлийн дайчидаа?” Ахлангаа Линлей “Би ирээдүйд гэгээнтэн зэрэглэлийн дайчин болох болов уу?” гэж бодож байв.

“Линлей” Хиллманий хоолой хойноос сонсогдов. Хиллман Рожер Лорри гурав түүний гүйцээд ирэв.

Линлей баяртайгаар “Авга Хиллман”

Үүний дараа Линлей гүнзгий амьсгаа авснаа толгойгоо өргөн Хиллманыг хараад. Маш эелдэгхэнээр тэрээр “Таны гэгээнтэн зэрэглэлийн дайчдийн тухай ярьсан үнэн үү? Тэгвэл миний тухай хэлээч. Би гэгээнтэн зэрэглэлийн дайчин болж чадах болов уу?” Линлейгийн зүрхэнд бүх хүүхэд хүсэх байсан хүслэн байлаа.

Хиллман хөшиж, түүний хажууд байсан Рожер Лорри хоёр хэлэх ч үггүй болов.

Гэгээнтэн зэрэглэлийн дайчид аа?

“Энэ хүүхэд үнэхээр том мөрөөдөх юм. Фэнлай улс сая сая иргэдтэй. Олон зууны турш нэг ч гэгээнтэн зэрэглэлийн дайчин гарч ирээгүй. Бүр гэгээнтэн зэрэглэлийн дайчин болохыг хүсч байна шүү.” Хиллман гэгээнтэн зэрэглэлийн дайчин болох ямар хэцүүг мэдэж байлаа.

Маш багаасаа маш шаргуу ажиллаж, язгууртан овгийн тусламжтай, маш өндөр ур чадвар мөн мэдээж аз шаарддаг зүйл. Хэн дуртай нь яаж гэгээнтэн зэрэглэлийн дайчин болох билээ?

Зургадугаар зэрэглэлийн дайчин болохыг тулд маш олон амь тэмцсэн тулаанд орж, ямар их зовснийг мэдэж байгаа. Зургадугаар зэрэглэлийн дайчин болох хүртэл хэцүү. Долоо, найм, есдүгээр зэрэглэл үүнээс ч хэцүү. Гэгээнтэн зэрэглэлийн дайчин. Мөрөөдөлдөө ч Хиллман энэ тухай боддоггүй билээ.

Гэхдээ Линлейн тууштай харцийг хараад.

“Линлей ах нь чамд итгэж байна. Чи мэдээж гэгээнтэн зэрэглэлийн дайчин болно.” Линлей уруу ширтэн Хилман шулуухан хэллээ. Энэ үгс Линлейн нүдийг гялалзуулан урам зориг өглөө. Линлейн зүрхэнд мэдээж маш их хүслэн бадарлаа.

Хэзээ ч урьд нь хүсч байгаагүй тийм хүчтэй хүслэн.

“Авга Хиллман маргаашнаас би дасгалуудаа арван настай хүүхдүүдтэй цуг хийж болох уу?” Линлей гэнэтхэн асуулаа.

Хиллман, Рожер, Лоори бүгд Линлей уруу гайхшран харлаа.

“Язгууртан аав минь хэлэхдээ өөртэй чинь эн зэрэгцэх хүн байхгүй болохыг хүсвэл бүх хүнээс илүү шаргуу сургуулилах ёстой гэж сургадаг.” Линлей өөрийн эрхгүй аавынхаа хэлсэн үгийг хэллээ.

Хиллман гэнэтхэн инээмсэглэл тодруулан. Өнөөдрийн Линлейн дасгалын үр дүнг харвал хэдийгээр Линлей зургаахан настай ч тэрээр есөн настай хүүхдийх шиг биетэй гэж бодном. Хиллман даруй толгой дохин “Болж байна. Чи залхуурах хэрэггүй шүү. Энэ бол ганц хоёр өдрийн хичээл зүтгэл биш, урт хугацааны хичээл зүтгэл гэдгийг ойлгох хэрэгтэй.”

Линлей жижигхэн толгойгоо бардам өргөн. Өөртөө итгэлтэйгээр “Авга Хиллман та харж л байгаарай!”гэж хэллээ.

Вушаны жирийн л нэгэн өглөө ингэж өнгөрлөө. Үүнээс цааш энэ өглөөтэй бүх өглөө адилхан үргэлжилнэ. Хилманийг Вушаны хүүхдүүд даган түүний удирдлаган дор суралцлаа. Энэ өглөөнөөс ганц ялгаатай зүйл нь гэвэл зургаан настай Линлей голын бүлгийн арван настнууд дунд орж ирсэн юм.


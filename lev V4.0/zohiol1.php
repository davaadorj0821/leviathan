Тэнгэрт эх туулайн тэргэл сар мандсан байлаа. Бүжингээ тэнгэрээс харах туулай мэт дүрс саранд сийлээстэй. Өдөр шиг гэгээтэй энэ шөнө далайн давалгаа мэт үүлс тодхон харагдана. Зуны урь ялимгүй гарч шөнө сэрүү татсан байлаа. Моддын ногоон навчис хүрээлсэн хорооллын цэцэрлэгийн урд есөн давхар цагаан байр байна. Долоон давхарын гурван өрөө байранд Отгонбаяр ажлаасаа ирэн хүүхдүүдтэйгээ хамт байх авч, бүх зүйл өнгөрсөн шигээ аз жаргалтай, дулаан уур амьсгалтай биш байна. Отгонбаяр туранхайвтар хав хар үс, цагаан царайтай хүн байлаа. Түүний сахал ургаж сөрдийж, нүдэн доор хөх туяа татсан байлаа.

Гэрээр дүүрэн хиншүү үнэртэж байв. Учир нь Отгонбаяр хүүхдүүддээ хийж өгч байсан цуйван саван доороо наалдаж, түлэгджээ. Эцэст нь учир зүйгээ олцгоон оройн хоолоо идсэн гэр бүлийнхэн унтахаар бүгд хэвтээд өгчээ.

Хүүхдийн чимэглэлтэй унтлагын өрөөнд эгч дүүс хоёр давхар оронд унтахаар хэвтсэн байлаа. Баяр хүүгийн эгч аль хэдийн бат бөх нойрондоо хулжсан байв. Харин Баяр хүү ээжийгээ санан үгүйлж орондоо хөрвөөж унтаж чадахгүй орондоо хий дэмий санан бэтгэрч байлаа.

Дөрвөн настай хүүгийн булбарай хацраар нулимс урсан дэрийг норгоход түүний толгойд ээж нь яагаад өөрийг нь орхин явсан талаар олон бодол эргэлдэнэ. Хүү ээжийгээ бодон нүдээ анин уйлахтай зэрэгцэн тэргэл сар тэнгэрт мандсан байлаа. Түүний нүдэнд өөд нь байх том цонхоор орж ирсэн тэргэл сарны туяа тусахад Баяр хүү нүдээ нээн, орноосоо өндийн цонхий зүг алхана. Цонхоор харахад бүх зүйл намуухан. Тэнгэрт түгэх одод анивчиж, их хотын гудамжны гэрлүүд гэрэлт цох мэт санагдана. Хотын цонхоор хэсэг харж зогссоноо хүү эргээд орондоо орох гээд эргээд харлаа. Хачирхалтай нь эргээд хартал хүүгийн бие орон дээрээ хэвтсэн чигээрээ байх юм.

Гайхах, айх зэрэгцэн юу болсныг ухаараагүй хүүд бүх бие нь хөнгөрч, дүүлэн нисэх мэт эрх чөлөөтэй мэдрэмж төрлөө. Тэрээр эгчийнхээ хажууд очин эгчийгээ сэрээх гэж оролдон орилох авч эгч нь сэрэхгүй байлаа. Эгчийгээ сэрээх гэж дахин орилж, эгчдээ хүрэх гэтэл хүүгийн гар эгчийнхээ биеийг нэвт гарлаа. Яг л хий үзэгдэл болсон мэт хагас тунгалаг өнгөтэй түүний гар эгчдээ хүрч чадахгүй биеийг нь нэвтэлсээр. Хүү юу болж байгааг ойлгохгүй айн, цочирдов. Өөрийнхөө биеийг дахин нэг харлаа. Хэрэв өөрийн биедээ хүрвэл буцаж орчих юм шиг хүүд санагдаж байв. Хүү биедээ буцаж орох гэж байтал…

Зурсхийн орох ганц бодол бол холын тэртээ явсан ээжийгээ хайх байв. Хүү сүнсэн биеэрээ цонх уруу аажмаар дөхлөө. Хүү цонхоор их хотын туяарах гэрлүүдийг, оддын анивчих гэрлийг харна. “Ээж минь яг хаана байгаа юм бол? Аав хэлэхдээ ээж минь маш хол явсан гэсэн. Яг хаашаа явсан юм бол?” Баяр хүү цонхоор төвөггүй нэвтэрч гарлаа. Хүүгийн сүнс өндөрт нисч байв. Хаашаа бол хаашаа явах цаг хугацааны асуудал болжээ.

Улаанбаатараас эхлээд ээжийгээ хайж үзэхээр шийдлээ. Байшингуудийн цонхоор маш хурдтай нэвтэрч орон унтаж буй хүмүүсийг ажлаа. Хүүгийн бие яг л үл хүрэгдэм хагас тунгалаг бие учраас ямар ч саадгүй хатуу биетээр нэвтрэн гарч байв. Харамсалтай нь маш удаан хайсний эцэст хотын аль ч байранд хүүгийн ээж байсангүй. Дөрөвхөн настай балчир хүү аавынхаа хэлсэн “Ээж нь их хол явсан” гэдэг үгийг дахин санан ээжийгээ хаа хол явсан юм бол? гэж гайхан бодлогоширсноо…

“Аан хотод байхгүй юм байна!” гэж санаад хаана байж болох талаар эргэцүүлнэ…

Хүү ээжийгээ хаа байгааг олохгүйгээр барахгүй ээжийнхээ хаана байж болох талаар ганц ч таамаг хүүд байхгүй байв. Бүр холоо явсан юм болвуу хэмээн хотоос гаран ээжийгээ хайлаа. Дээшээ дүүлэн нислээ. Хотын дээгүүр, уул толгодын хажуугаар хотоос гаран хайж байлаа. Хэсэг газруудаар хайсан боловч ой модод, гол мөрний ай савын хаана ч хүү ээжийгээ олж чадахгүй байв. Хүү аргаа баран дээш оддыг харан, ногоон зүлгэн дээр тэнийн хэвтлээ.

Гэрэлт шөнийн анивчих одод ер бусын үзэсгэлэнтэй харагдаж байлаа. Хүү нүдээ гүйлгэн од бүрийг сайтар ажина. Гэтэл холын тэртээ тэнгэрийн оддоос нэгэн од бусдаас содон санагдлаа. Тэр од бусдаас онцгой анивчина. Илүү хурц, илүү тод, илүү хурдан анивчих тэр одыг хараад хүү “Арай ээж минь энэ одонд байгаа юм байхдаа?” гэж санаад тэр одруу нислээ.

Бусад оддоосоо содон тэр од уруу ойртох аваас энэ од үнэндээ тэнгэрт байх од биш, харин агаарт байх хаалга байх нь тодрон харагдана. Тэрхүү хаалганы хажууханд ирвэл хаалганы үүд нээлттэй. Дотроос нь хүчтэй гэрэл цацран, өдөр болж буй цэлийсэн хот харагдлаа.

“Тэнгэрт яагаад хаалга байгаа юм бол? Яагаад хаалганы дотор өдөр болж байгаа юм бэ? Бас яагаад энэ тэнгэрт хот байдаг билээ?” гэж маш олон асуулт хүүд гарч ирнэ. Хүү амьдралдаа хэзээ ч ийм сонин хачин зүйл харж байгаагүй. Гэтэл энэ хаалга яагаад ч юм ээж уруу нь хүргэх хаалга мэт хүүд санагдлаа. Хүү хаалганы цаана зөв сонингоо даган хөл тавин орсноор бүх зүйл гэрэлтэй болж, өдөр угтсан шинэ хот Баярын өмнө налайх нь энэ.

Эртний европ загварын сүрлэн дээвэртэй чулуун барилгууд, хүний нүүрний хэсэгт байх нүд, хамар, чих тэр ч бүү хэл хүний амтай гудамж талбай, мөн хүний хэлээр хийсэн мэт зөөлхөн замтай байв. Өмхийрч ялзарсан махны үнэр ханхалсан тэрхүү гудамжинд хүмүүс холхилдох ажээ. Зарим хүмүүс хуучин социалист загварын хувцас өмссөн байв. Гэвч дийлэнхи хүмүүс орчин үеийн хувцастай байх ажээ.

Хотын захаас хотын төврүү алхах жаалхүү хүн бүрийг ажина. “Энэ хүмүүсийн дунд ээж хаана байгаа бол?” гэж гайхан алхана. Дөрөвхөн настай хүү яг л аврага үлэмж биетнүүдийн дунд орсон мэт жижигхэн биетэй тул зөвхөн эгц дээшээ харан байж хүмүүсийн царайг харж байлаа. Хүүгийн нүдэнд зөрөн өнгөрөх олон хүн харагдавч энэ хүмүүсийн дунд хайртай ээж нь байхгүй байжээ.

Хүүд яагаад энэ хүмүүсийн нүдэнд гуниг харуусал тодрон дүүрсэн нь хачирхалтай санагдаж байв. Яг л харууслын утаа сүүмийлзүүлэх харц хотоор нэг дүүрэн байв. Хүү нүдээ аниад, гүнзгий аваад дээшээ толгойгоо гижийлгээд орчин тойрноо мэдрэхийг хүслээ. Нарны дулаан илч хүүгийн нүүрэнд тусч, ялзарсан өмхий махны үнэх ханхалж, хөлд маш зөөлөн хийлсэн талбай дээр гишгэж байгаа мэт санагдана. Энэ газрын цаг агаар маш дулаан чийглэг байх бөгөөд хүү нүдээ нээхэд тэнгэр хөх биш улаан, үүлс нь цагаан биш хар байх юм. Хаана ирснээ мэдээгүй хүү ээжийгээ энэ газраас олохгүйгээ гадарлаж буцах гэж эргэж харах мөчид түүний сэтгэлд хоногсон дулаахан хоолойгоор “Баяраа миний хүү” гэж дуудах сонсогдоно.

Эргээд хартал Баярын ээж үр зулзагаа үхэлд алдаад дахин амилсныг нь харж байгаа энэрэлт эх хүн шиг мишээж баярлах нь харагдана. Хүүгийн ээж бол тунгалаг бор нүдтэй, бор үстэй, туранхай, өндөр, нүдэнд дулаахан эмэгтэй юм. Баярын ээж үрээ тосон тэврэх гэж доош суун, гараа тэнийлгэв. Харин хүү хайгаад олдохгүй байсан ээжрүүгээ бушуухан гүйн очиод тэврэн авахад өөрийн эрхгүй хүүгийн нүдэнд аз жаргалын оч дүрэлзэх нь энэ. Олон сар ээжтэйгээ уулзаагүй авч ээжийнх сайхан үнэр нь яг л хэвээрээ байх ажээ. Ээж хүү хоёулангийн нүдэнд аз жаргалын оч бадарч инээмсэглэн баярлана. Энэ мэдрэмж бол хамгаас сайхан аз жаргалын амт юм.

“Миний хүү энд юу хийж байгаа юм бэ” гэж Баярын ээж нүдээ том болгон сониучирхан хүүгээсээ асууна.

Нэгдүгээр хэсэг – Эх үрийн холбоо

Үхлийн талаар олон бодол байдаг. Энэ бодлуудаас хүмүүс айдсийг мэдэрдэг. Ялангуяа хүмүүс үхлийг бүх зүйлийн төгсгөл гэж хүлээж авна. Харин үхэл аливаа зүйлийн эхлэл болдогийг бид санах хэрэгтэй.

“Бидний ертөнцтэй олон ертөнц зэрэгцэн оршдог. Олон ертөнцүүдэд ялгаатай олон амьтад амьдарч байна. Энэ бүгдийг би хараагүй бол мэдэхгүй л явах байлаа…”

Энэ түүх дөрвөн настай хүү ээжийгээ өвчнөөр алдсанаар эхэлсэн. Хорвоо дэлхий хүүд үндсээрээ эргэж нэг л гунигтай болов. Эхийнхээ тэвэрт хайранд умбадаг байсан хүүгийн хувьд энэ том цохилт юм. Хүүг Баяр гэдэг. Ээжийгээ дуурайсан том хар нүд, аавыгаа дуурайсан цэвэрхэн шар арьс, хав хар үстэй байлаа.

Хүү ээжийгээ алдсанаас хойш аав эгч хоёртойгоо л хоцорсон. Өдөр бүр түүнийг халамжилдаг байсан хамгаас дотно ээж нь байхгүй учраас хүүд маш гунигтай байв. Түүнд цаг хугацаа удаан өнгөрч, бараан үүл амьдралыг нөмрөн авсан мэт санагдана. Баяр хүүгийн баяр хөөртэй харц гунигаар дүүрч, цовоо сэргэлэн царай нь гундуухан явах боллоо.

Яг энэ үед харин хоёр хүүхдийн аав хайрт эхнэрээ алдсан. Тэрээр хоёр хүүхдээ өсгөх маш том ачааг ганцаар үүрэх болов. Аав нь хоёр хүүхдээ сайхан, хангалуун өсгөх гэж борви бохисгүй ажиллана. Сарын хагаст Өмнөговь аймагт эртний чулуужсан модны үлдэгдэл, үлэг гүрвэлийн чулуужсан ясыг эрэн хайдаг археологич ажилтай. Харин сарын сүүлд хоёр хүүхэдтэйгээ хамт цагийг өнгөрүүлэнгээ эртний олдворуудаа судалдаг ажилтай болжээ.

Эгч дүүс харин аавыгаа эзгүйд авга ахындаа амьдрах болно. Мэдээж өссөн гэр биш учраас эгч дүүст тав тухгүй байдаг байв. Ээжийгээ алдсанаас хойш Баярын эгч Гэрэл ихэд ухаан сууж дүүгээ харж ханддаг байлаа. Газар дээр ганц хамгаалах ёстой дүүдээ Гэрэл ээж шиг нь энэрэлтэй, аав шиг нь нөмөртэй байх боллоо.


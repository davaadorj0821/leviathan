“Гоё доо, ээжээ та ёстой амттай хоол хийдэг. Аав дандаа хоолоо түлчихээд байдаг юм.” танан цагаан шүдээ ярзайлган Баяр хүү хариулав.
“Аав нь хоол хийхдээ муу байсан одоо ч муу хэвээрээ байгаа юм уу?” гэж ээж нь шоолоод
“Та хоёр минь амттай юм идээгүй удсан байх даа. Миний хүү эгч нь өвдөөгүй биз дээ?” хэмээн санаа зовсон мөн сониучирхсан байдалтай хүүгээсээ асуулаа.
“Тиймээ, аав муу хоол хийдэг. Эгч бид хоёрт үргэлж амтгүй хоол хийж өгнө. Эгч харин ааваас амттай хоол хийдэг.  Эгчийн бие сайн байгаа.” гэж нарийнхан хүүхдийн хоолойгоороо хариуллаа.

Ээж хүү хоёр ингээд хөтлөлцөөд алхлаа. Үл мэдэгдэх хотод байх аз жаргалтай ээж хүү хоёр ганцаар энэ хотод байгаа мэт. Гэхдээ Баяр хүүгийн нүдэнд амьдралдаа харж байгаагүй хачирхалтай зүйлс туслаа. Зарим байшингуудийн цонх хүний нүдтэй ба хотоор алхах хүмүүсийг ширтэж, нүдээ ирмэх юм. Мөн хананы зарим хэсэгт хаанаас ч юм мэдэгдэхгүй чих ургачихсан байна. Зарим хаалга хүний ам шиг хэлбэртэй байх бөгөөд зарим нь бүр илт мэдэгдэхүйц уруултай байна. Баярын нүдэнд энэ үл мэдэгдэх хотын гаж этгээд зүйлс гайхшрал төрүүлсэн ч удаан уулзаагүй ээж нь хамгаас анхаарал татаж байв. Хажууд нь байгаагүй удсан болоод тэр үү? Эсвэл маш ихээр үгүйлсэн болоод энэ үү? Баяр ээжийгээ л харан харсаар түүнийг дагаад гэрт нь очив.

Европ маягын сүрлэн дээвэртэй жижигхэн залгаа байшингуудийн нэг ээжийнх нь гэр байв. Хаалгаар хөл гишгэн орвол хүү нүдэндээ ч итгэсэнгүй. Яг л долоон давхрын хорооллын байртай нь адилхан байр байв. Баяр ээж аав эгчтэйгээ амьдардаг гэр нь яг дүрээрээ байлаа. Аавын өрөө, эгчтэйгээ хамт унтах өрөө яг хэвээрээ. Гэрийнх нь бүх тавилгууд ч яг тэр чигээрээ байна. Гэртээ ирсэн мэт байх боловч хүүгийн эгч аав хоёр мэдээж энд алга байна.Ганц энэ байрны ялгаа бол цонхоор харахад гадна талд ногоон моддоор хүрээлүүлсэн цэцэрлэг биш харин үл мэдэгдэх нууцлаг хот харагддаг байв.

Элэг бүтэн ээжтэйгээ хамт амьдардаг байсан шигээ хүү ээж хоёр өдөржингөө цагийг жаргалтай өнгөрүүлнэ. Цуг хоол хийж идэн, хүүгээ эрхлүүлэн, хүүтэйгээ эвлүүлдэг тоглоомоор байшин барьж хотын захирагч болон тоглоно. Цагийн зүү харвасан сум мэт өнгөрч нэг л мэдэхэд үл мэдэгдэх хотын улаан нар жаргах болов.

“Миний хүү одоо гэртээ харь. Харанхуй болж байна, энд харанхуй болоход үлдэж болохгүй. Дараа өглөө ээж дээрээ ирнэ биз дээ?” гэж ээж нь санаа зовсон байдалтай хөмсгөө үл мэдэгдэм атируулан хүүгээсээ асуулаа.
“Ээжээ та эндээ үргэлж байх юмуу? Та яагаад буцаж ирж болохгүй юм бэ?” гэж Баяр алаг нүдээ тормолзуулан гайхах, сониучирхах зэрэгцэн асуулаа.
“Ээж нь буцаж очиж болохгүй ээ. Миний хүү харин над дээр яаж ирсэн юм бэ?” Хэдийгээр  урт хугацаа өнгөрсөн ч сая л хүүтэйгээ уулзсан мэт яаж хүүгээ ирсэнийг Сарантуяа эцэст нь асуулаа.
“Ээжээ би тань дээр өнөөдөр ирсэн шиг ирье тэгвэл. Маш амархан биеэ үлдээгээд ирэхэд болно.” Баяр бахархалтай, хөгжилтэй аргаггүй цоглогхон хариуллаа.

Үл мэдэгдэх хотын цэлмэг саруул тэнгэрт наран жаргаж, сэлүүхэн хотоор хүүгээ хотын захад байх хаалга уруу ээж нь гаргаж өглөө. Хүү ирсэн замаараа буцаж, уул толгодын дэргэдүүр нисэн хотын дээгүүр дүүлэн нисч гэртээ эргээд ирлээ. Хотод үүр цайхын алдад хүү орон дотроо хэвтэх өөрийн биедээ эргэн орлоо. Баяр нүдээ аажуухан нээн орчин тойрноо ажив. Бүх зүйл яг л бодит мэт санагдана. Гэвч үүр цайгаагүй нэгэнт үд дунд болсон байв.

Зүүд эсэхийг мэдэхгүй ч түргэвчлэн Баяр эгч аав хоёртоо болсон явдлыг ярьж өгөв.Аав нь “зүүд байлгүй дээ” гэж дотроо бодоод итгэсэнгүй. Мөн арван настай эгч нь дүүгээ зүүдэлсэн гэж дотроо бодсон ч итгэсэн дүр үзүүлэв. Хэн дөрөвхөн настай хүүгийн энэ үнэмшмээргүй үгэнд итгэх билээ?

—

Дэлхийд цаг хугацаа удаан өнгөрөх мэт санагдана. Хүү орой болохыг тэсэн ядан хүлээж байв. Учир нь Баяр ээжтэйгээ уулзсан өнгөрсөн шөнийн явдлаа зүүд эсэхийг шалгахыг хүсч байлаа. Наран жаргаж унтах цагийг тэсгэл алдан хүлээнэ. Баяр өнгөрсөн шөнийн болсон явдлыг зүүд гэж бодохгүй байлаа. Даанч Баярт ямар ч сонголт байхгүй. Үүнийг зүүд эсэхийг унтах гэж хэвтэж байж мэднэ.

Нар буухыг хармагцаа унтах гэж хүү орондоо яаран оров. Биенээсээ гарах гэж их хичээсэн ч бүтэлтэй болсонгүй. Хүү өнгөрсөн шөнө хэрхэн биеэ орхин гарсан тэр л мэдрэмж дээрээ төвлөрөв. Гүнзгий амьсгаа аван нүдээ нээн босоход хүү биеэ амжилттай орхин гарлаа.

Намуухан шөнө тэнгэрт байх тэнгэрийн хаалга уруу очин, өнөөх нууцлаг хотод эргэж ирэхэд муухай өмхийрсөн махны үнэр тосч авлаа. Нүдэндээ гунигийн утаа тээсэн хүмүүс мөн л хотоор холхилдоно. Хотын арваад хүн багтчихмаар гудамжуудаар алхан өнгөрсөн шөнө дайрч зөрсөн байшин, эргэлт болгоноор явсаар хүү ээжийнхээ гэрийн гадаа ирлээ.

Баярын хувьд хамгийн ихээр айж байгаа зүйл бол хаалганы цаана хайрт ээж нь байгаа боловуу гэсэн бодол. Хаалганд Баяр хүү дөхөн очиод хаалганд байх чихэнд “би ирлээ” гэж шивнэв. Хаалганы цаанаас хөлийн чимээ түг, түг, түг гэж сонсогдож байснаа хаалганы өмнө ирээд аниргүй болов. Харин хаалганы бариул доош эргэн хаалга онгойлоо. Баяр хүү энэ мөчид хаалганы цаана хэн байх бол гэсэн бодолдоо автсан хэвээр байв. Хаалга аажуухан онгойж харвал  түүний хамгаас хайртай ээж мөн байв.
